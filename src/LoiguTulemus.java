public class LoiguTulemus {
    private String loik;
    private int skoor;

    public LoiguTulemus(String loik, int skoor) {
        this.loik = loik;
        this.skoor = skoor;
    }

    public String getLoik() {
        return loik;
    }

    public int getSkoor() {
        return skoor;
    }

}
